m=int(input("enter first number:"))
n=int(input("enter second number:"))
op=input("enter operator:")
if op=='+':
  print("Addition of two numbers =",m+n)
elif op=='-':
  print("Subtraction of two numbers=",m-n)
elif op=='*':
  print("Multiplication of two numbers=",m*n)
elif op=='/':
  print("Division of two numbers=",m/n)
